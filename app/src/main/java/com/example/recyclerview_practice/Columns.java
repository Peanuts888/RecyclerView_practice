package com.example.recyclerview_practice;

public class Columns {

    private long id;
    private String date;
    private String body;

    public long getId() {
        return id;
    }

    public String getDate() {
        return date;
    }

    public String getBody() {
        return body;
    }

    public void setId(long id) {
        this.id = id;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public void setBody(String body) {
        this.body = body;
    }
}
